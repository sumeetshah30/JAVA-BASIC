

class rectangle{
		private int length;
		private int breadth;
		
		public rectangle() {
			this.length=4;
			this.breadth=5;

		}
		public rectangle(int length,int breadth) {
			this.length=length;
			this.breadth=breadth;
		}
		public int getlength() {
			return length;
		}
		public int getbreadth() {
			return breadth;
		}
}

